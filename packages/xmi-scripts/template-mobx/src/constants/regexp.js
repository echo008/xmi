// 邮箱
export const REG_EMAIL = /^[\w.-]+@[\w-]+(\.[a-zA-Z0-9-]+)*\.[a-zA-Z0-9]{2,}$/

// 密码：8-18位数字、大小写字母、符号任意两种组合
export const REG_PASS = /^(?![0-9]+$)(?![a-z]+$)(?![A-Z]+$)(?!([^(0-9a-zA-Z)]|[()])+$)([^(0-9a-zA-Z)]|[()]|[a-z]|[A-Z]|[0-9]){8,18}$/i

// 手机号码
export const REG_MOBILE = /^1[0-9]{10}$/

// 电话号码（带区号）
export const REG_PHONE = /^(0[0-9]{2,3}-)?([2-9][0-9]{6,7})+(-[0-9]{1,8})?$/
